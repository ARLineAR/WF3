// Opérateur d'affectation : =
var myNumber = 10;
console.log(myNumber);

// Opérateur d'incrémentation : ++
myNumber++;
console.log(myNumber);

//  Opérateur de décrémentation : --
myNumber--;
console.log(myNumber);

// Opérateur d'addition et de concaténation : +
var myAdd = 7 + 5;
console.log(myAdd);

var myConc = 'Salut ' + 'mon ' + 'pote';
console.log(myConc);

// Si on concatène un STRING et un NUMBER => résultat 75
var myAdd = '7' + 5;
console.log(myAdd);

// Opérateur de soustraction : -
var mySous = 7 - 5;
console.log(mySous);

// Opérateur de soustraction d'un STRING et un NUMBER => résultat 'NaN' (Not a Number)
var mySous = 7 - 5;
console.log(mySous);

// Opérateur de multiplication : *
var myMult = 7 * 5;
console.log(myMult);

// Opérateur de division : /
var myDivi = 14 / 3;
console.log(myDivi);

// Opérateur modulo : %
var myModulo = 12 % 5;
console.log(myModulo);

// Opérateur d'addition et d'affectation : +=
var first = 10;
var second = 5;

// a = a + b;
// a += b;
// console.log(a);

// first = first + second;
first += second
console.log(first);

// Opérateur de soustraction et d'affectation : -=
second -= first;
console.log(second);